function changeDrpDwn1() {
    var checkbox = document.querySelector('.switchPlan input[type="checkbox"]');

    if (checkbox.checked) {
    var titleChangeElement = document.getElementById("freePricing1");
    titleChangeElement.textContent = "5 Pro";


    var priceChangeElement = document.getElementById("free3");
    priceChangeElement.textContent = "£25";

    var drpMenuElement = document.getElementById("dropbtn3");
    drpMenuElement.textContent = "Up to 5 Clients";
    } else  {
    var titleChangeElement = document.getElementById("freePricing1");
    titleChangeElement.textContent = "5 Pro";
    
    var priceChangeElement = document.getElementById("free3");
    priceChangeElement.textContent = "£30";
    
    var drpMenuElement = document.getElementById("dropbtn3");
    drpMenuElement.textContent = "Up to 5 Clients";
    }

}

